    private void btnBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBActionPerformed


        
        Connection conect = null;
        
        try{
            
            Connection con = getConnection();
            ps = con.prepareStatement("SELECT * FROM articulos WHERE Nombre = ?");
            ps.setString(1, txtB.getText());
            
            rs = ps.executeQuery();
            
            if(rs.next()){
                txtid.setText(rs.getString("Id_articulo"));
                txtNom.setText(rs.getString("Nombre"));
                txtDes.setText(rs.getString("Descripcion"));
                txtEst.setText(rs.getString("Estado"));
                txtCate.setText(rs.getString("Categoria"));
                Image i=null;
                Blob blob=rs.getBlob("Foto");
                i=javax.imageio.ImageIO.read(blob.getBinaryStream());
                ImageIcon image=new ImageIcon(i);
                jlf.setIcon(image);
                txtRuta.setText(rs.getString("Ruta"));
            }
                
                else{
                        JOptionPane.showMessageDialog(null, "No Existe el Articulo que Busaca");
            }
            
            txtB.setText("");
        }    
        catch(Exception e){
            System.err.println(e);
            
        }
        
    }//GEN-LAST:event_btnBActionPerformed

