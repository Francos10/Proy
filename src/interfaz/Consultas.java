    private void btnMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMActionPerformed
        
      Connection conect = null; 
        
        try{
            
            Connection con = getConnection();
            ps = con.prepareStatement("UPDATE articulos SET Id_articulo=?, Nombre=?, Descripcion=?, Estado=?, Categoria=?, Foto=?, Ruta=? WHERE Id_articulo=?");
            ps.setString(1, txtid.getText());
            ps.setString(2, txtNom.getText());
            ps.setString(3, txtDes.getText());
            ps.setString(4, txtEst.getText());
            ps.setString(5, txtCate.getText());
            ps.setString(8, txtid.getText());
            
            FileInputStream archivoFoto;
            archivoFoto = new FileInputStream(txtRuta.getText());
            ps.setBinaryStream(6, archivoFoto);
            ps.setString(7, txtRuta.getText());
            
            
            
            int res = ps.executeUpdate();
            
            if(res > 0){
                JOptionPane.showMessageDialog(null, "Se ha realizado la Modifiacacion");
                //limpiar();
            }
            else{
                JOptionPane.showMessageDialog(null, "No se realizo la Modificacion");
                //limpiar();
            }
            
            con.close();
            
        }
        catch(Exception e){
            System.err.println(e);
            
        }
       
    }//GEN-LAST:event_btnMActionPerformed

